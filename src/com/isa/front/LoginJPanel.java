    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try{
            String pass = new String( jPasswordField1.getPassword());
            if ( Utiles.isNullOrEmpty(pass)){
                mostrarMensajeError("Debe ingresar el pin del token.");
                return;
            }
            
            HandlerToken handlerToken = ActualCertInfo.getInstance().getHandler();
            ActualCertInfo.getInstance().setPassword( pass );
            
            Token token = handlerToken.getTokenActivo();
            token.login(ActualCertInfo.getInstance().getPassword());
            
            ActualCertInfo.getInstance().setPassword( pass );
            ManejadorPaneles.showPanel(ListaCertsJPanel.class.getName());
            
            
        }
        catch ( IOException ex) {
            
            if (ex.getCause() instanceof LoginException){
                LoginException log = (LoginException) ex.getCause();
                PKCS11Exception pkcs = (PKCS11Exception) log.getCause();
                if ( Utiles.PKCS11_EXCEPTION_CKR_PIN_INCORRECT.equals(pkcs.getMessage())){
                    mostrarMensajeError( UtilesMsg.ERROR_PIN_INCORRECTO );
                }
                if (Utiles.PKCS11_EXCEPTION_CKR_PIN_LEN_RANGE.equals(pkcs.getMessage())){
                    mostrarMensajeError( UtilesMsg.ERROR_PIN_INCORRECTO );
                }                
                if (Utiles.PKCS11_EXCEPTION_CKR_PIN_LOCKED.equals(pkcs.getMessage())){
                    mostrarMensajeError( UtilesMsg.ERROR_TOKEN_BLOQUEADO);
                }
                if (Utiles.PKCS11_EXCEPTION_CKR_TOKEN_NOT_RECOGNIZED.equals(pkcs.getMessage())){
                    mostrarMensajeError(UtilesMsg.ERROR_TOKEN_SIN_IDENTIFICAR);
                }
            }            
            Logger.getLogger(LoginJPanel.class.getName()).log(Level.SEVERE, null, ex);
        } 
        catch (NoSuchAlgorithmException ex) {
            mostrarMensajeError(UtilesMsg.ERROR_AUTH);
            Logger.getLogger(LoginJPanel.class.getName()).log(Level.SEVERE, null, ex);
        } 
        catch (CertificateException ex) {
            mostrarMensajeError(UtilesMsg.ERROR_AUTH);
            Logger.getLogger(LoginJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    
    private void mostrarMensajeError( String mensaje ){
        msjLogin.setText( mensaje );
        msjLogin.setVisible(true);
        msjLogin.setForeground(Color.RED); 
    }


